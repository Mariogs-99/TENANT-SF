  CREATE OR REPLACE TRIGGER "TRG_UPDATE_STATUS_RANGO_DTE" 
BEFORE INSERT ON CNRPOS_RANGO
FOR EACH ROW
DECLARE
ID_RANGO NUMBER;
RANGO_ACTUAL NUMBER;
BEGIN
	-- VALIDAMOS SI EXISTE UN RANGO DEL MISMO TIPO
	SELECT ACTUAL_VALOR, ID_RANGO INTO RANGO_ACTUAL, ID_RANGO  FROM CNRPOS_RANGO
	WHERE ID_TIPO_RANGO = :NEW.ID_TIPO_RANGO AND ACTIVE = 1
	AND ID_SUCURSAL = :NEW.ID_SUCURSAL
	ORDER BY ID_RANGO FETCH FIRST ROW ONLY;

	IF ID_RANGO > 0 THEN
		UPDATE CNRPOS_RANGO SET ACTIVE = 0 WHERE ID_TIPO_RANGO = :NEW.ID_TIPO_RANGO AND ID_SUCURSAL = :NEW.ID_SUCURSAL;

		IF RANGO_ACTUAL > :NEW.ACTUAL_VALOR THEN
		     :NEW.ACTUAL_VALOR := RANGO_ACTUAL;
		END IF;
	END IF;

	EXCEPTION
    WHEN NO_DATA_FOUND THEN
        NULL;
    WHEN OTHERS THEN
        -- Manejo de otras excepciones
        RAISE_APPLICATION_ERROR(-20002, 'Error en el trigger TRG_UPDATE_STATUS_RANGO_DTE: ' || SQLERRM);
END;
