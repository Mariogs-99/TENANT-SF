        SELECT 44 AS ID_TIPO_RANGO, 'FACTURA DE SUJETO EXCLUIDO' AS VALOR FROM DUAL
    ) LOOP
        SELECT RANGO_SEQ.NEXTVAL INTO v_id_rango FROM DUAL;

